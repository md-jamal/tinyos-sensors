

#ifndef TLS2671_DRIVER_H
#define TLS2671_DRIVER_H

enum{
	TLS2671_I2C_ADDR = 0x39,
};

//Registers

enum{
	COMMAND_VALUE		= 0x80,
	ENABLE_REG		= 0x00,
	PROXIMITY_TIME_REG	= 0x02,
	WAIT_TIME_REG		= 0x03,
	PILTL_REG		= 0x08,
	PILTH_REG		= 0x09,
	PIHTL_REG		= 0x0a,
	PIHTH_REG		= 0x0b,
	PERSISTENCE_REG		= 0x0c,	
	CONFIG_REG		= 0x0d,
	PROXIMITY_CNT_REG	= 0x0e,
	CONTROL_REG		= 0x0f,
	DEVICE_ID_REG		= 0x12,
	DEVICE_STATUS_REG	= 0x13,
	PROXIMITY_DATA_LOW	= 0x18,
	PROXIMITY_DATA_HIGH	= 0x19,
};


//Default Register values

enum{
	ENABLE_DEFAULT		= 0x00,
	PROXIMITY_TIME_DEFAULT	= 0xFF,
	WAIT_TIME_DEFAULT	= 0xFF,
	PILTL_DEFAULT		= 0x00,
	PILTH_DEFAULT		= 0x00,
	PIHTL_DEFAULT		= 0x00,
	PIHTH_DEFAULT		= 0x00,
	PERSISTENCE_DEFAULT	= 0x00,
	CONFIG_DEFAULT		= 0x00,
	PROXIMITY_CNT_DEF	= 0x10,	//better to have less than 32 to achieve maximum SNR
	CONTROL_DEFAULT		= 0x10,//0x30
};



enum{
	POWER_ON		=	0x01,
	PROXIMITY_ENABLE	=	0x06,
	WAIT_ENABLE		= 	0x08,
	PROXIMITY_INTR_ENABLE	= 	0x20,
};



#endif
